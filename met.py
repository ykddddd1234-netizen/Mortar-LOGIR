import numpy as np


# =========================================
# MET Layer Midpoints
# =========================================

z_nodes = np.array([

    100,    # 00
    350,    # 01
    750,    # 02
    1250,   # 03
    1750,   # 04
    2250,   # 05
    2750    # 06
])


# =========================================
# Density Ratios
# =========================================

density_ratios = np.array([

    1.00,   # 00
    1.00,   # 01
    1.00,   # 02
    1.00,   # 03
    1.00,   # 04
    1.00,   # 05
    1.00    # 06
])


# =========================================
# Temperature Ratios
# =========================================

temp_ratios = np.array([

    1.00,   # 00
    1.00,   # 01
    1.00,   # 02
    1.00,   # 03
    1.00,   # 04
    1.00,   # 05
    1.00    # 06
])


# =========================================
# Interpolated MET Ratios
# =========================================

def get_met_ratios(z):

    # =====================================
    # Above Line 06
    # =====================================

    if z > z_nodes[-1]:

        return 1.0, 1.0


    # =====================================
    # Interpolation
    # =====================================

    density_ratio = np.interp(

        z,

        z_nodes,

        density_ratios
    )

    temp_ratio = np.interp(

        z,

        z_nodes,

        temp_ratios
    )

    return density_ratio, temp_ratio
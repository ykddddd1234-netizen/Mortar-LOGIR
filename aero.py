import numpy as np


# =========================================
# Sigmoid
# =========================================

def sigmoid(

    M,

    Mc,

    k
):

    return 1.0 / (

        1.0 + np.exp(

            -k * (M - Mc)
        )
    )


# =========================================
# Cd(M)
# =========================================

def get_cd(

    M,

    a,

    params
):

    # =====================================
    # Parameters
    # =====================================

    C0 = params["C0"]

    C1 = params["C1"]

    A = params["A"]


    # =====================================
    # Subsonic Baseline
    # =====================================

    C_sub = (

        C0

        + C1 * M
    )


    # =====================================
    # Final Cd
    # =====================================

    Cd = (

        C_sub

        + A * a
    )


    return Cd
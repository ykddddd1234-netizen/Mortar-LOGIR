import numpy as np


# =========================================
# Sigmoid
# =========================================

def sigmoid(

    M,

    Mc,

    k
):

    return 1.0 / (

        1.0 + np.exp(

            -k * (M - Mc)
        )
    )


# =========================================
# Cd(M)
# =========================================

def get_cd(

    M,

    params
):

    # =====================================
    # Parameters
    # =====================================

    C0 = params["C0"]

    C1 = params["C1"]

    A = params["A"]

    Mc = params["Mc"]

    k = params["k"]


    # =====================================
    # Subsonic Baseline
    # =====================================

    C_sub = (

        C0

        + C1 * M
    )


    # =====================================
    # Transonic Activation
    # =====================================

    S = sigmoid(

        M,

        Mc,

        k
    )


    # =====================================
    # Final Cd
    # =====================================

    Cd = (

        C_sub

        + A * S
    )


    return Cd